@allure.feature('插屏广告模块')
@pytest.mark.advert
@pytest.mark.appapi
@pytest.mark.test
@pytest.mark.online
class TestAdvert(object):
    #测试的项目名称
    project_name = "appapi/"+ENV+"/interface"
    #测试的功能模块名称
    file_name = "advert"
    Pname = "appapi"

    #获取对应接口的测试数据
    file_path = ExcelHandler().get_excel_path(project_name,file_name)
    countIgnoreNum_data = ExcelHandler().get_excel_data_1(file_path,"countIgnoreNum")
    countClickNum_data = ExcelHandler().get_excel_data_1(file_path,"countClickNum")
    getInsertScreen_data = ExcelHandler().get_excel_data_1(file_path,"getInsertScreen")
    getOpenScreen_data = ExcelHandler().get_excel_data_1(file_path,"getOpenScreen")


    @pytest.mark.countIgnoreNum
    @pytest.mark.parametrize('item',countIgnoreNum_data)
    @allure.story('开屏/插屏广告统计-忽略总数+1')
    def test_countIgnoreNum(self,item,Pname):
        Iname='countIgnoreNum'
        # 获取每一行数并且发请求：
        response = RequestHandler().get_response(item,Pname,Iname)
        # 行为驱动标记：
        allure.dynamic.feature(item['case_title'])
        allure.dynamic.story(item['case_description'])
        # 动态加参：
        allure.dynamic.title(item['case_title'])
        allure.dynamic.description(
            "<b style='color:red;'>描述：</b>{}<br />"
            "<b style='color:red;'>请求的url：</b>{}<br />"
            "<b style='color:red;'>预期值：</b>{}<br />"
            "<b style='color:red;'>实际执行结果：</b>{}<br />".format(
                item['case_description'],
                getconf(Pname,ENV +'_url') + getconf(Pname,Iname),
                response[0],
                response[1]
            ))
        assert response[0] == response[1]

