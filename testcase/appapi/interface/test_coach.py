    Pname = "appapi"
    DataFile = Pname+"/"+ENV+"/interface"
    #测试的功能模块名称
    file_name = "coach"

    #获取对应接口的测试数据
    file_path = ExcelHandler().get_excel_path(DataFile,file_name)
    tipsNum_data = ExcelHandler().get_excel_data_1(file_path,"tipsNum")
    getCoachPlanList_data = ExcelHandler().get_excel_data_1(file_path,"getCoachPlanList")
    getCoachPlanDetail_data = ExcelHandler().get_excel_data_1(file_path,"getCoachPlanDetail")
    updateCoachStatus_data = ExcelHandler().get_excel_data_1(file_path,"updateCoachStatus")
